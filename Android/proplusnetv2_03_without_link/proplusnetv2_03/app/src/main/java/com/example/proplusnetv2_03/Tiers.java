package com.example.proplusnetv2_03;

public class Tiers {

    private String CodeTiers;
    private String NomSociété;
    private String IdTiers;
    private String Activité;


    public String getCodeTiers() {
        return CodeTiers;
    }

    public void setCodeTiers(String codeTiers) {
        CodeTiers = codeTiers;
    }

    public String getNomSociété() {
        return NomSociété;
    }

    public void setNomSociété(String nomSociété) {
        NomSociété = nomSociété;
    }

    public String getIdTiers() {
        return IdTiers;
    }

    public void setIdTiers(String idTiers) {
        IdTiers = idTiers;
    }

    public String getActivité() {
        return Activité;
    }

    public void setActivité(String activité) {
        Activité = activité;
    }
}
